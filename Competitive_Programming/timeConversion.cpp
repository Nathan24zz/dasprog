#include <iostream>
#include <string>

using namespace std;

int main(){
  string s;
  cin >> s; 
  
  if(s[8]=='A'){
    s.erase(8,2); 
    if (s[0]='1' && s[1]=='2'){s[0] = s[1] = '0';}
    if (s[0]='1'){s[0] = '1';}
    if (s[0]='0'){s[0] = '0';}
  }
  if(s[8]=='P'){
    s.erase(8,2); 
    if (s[0]=='0' && s[1]=='1'){s[0] = '1'; s[1] = '3';}
    if (s[0]=='0' && s[1]=='2'){s[0] = '1'; s[1] = '4';}
    if (s[0]=='0' && s[1]=='3'){s[0] = '1'; s[1] = '5';}
    if (s[0]=='0' && s[1]=='4'){s[0] = '1'; s[1] = '6';}
    if (s[0]=='0' && s[1]=='5'){s[0] = '1'; s[1] = '7';}
    if (s[0]=='0' && s[1]=='6'){s[0] = '1'; s[1] = '8';}
    if (s[0]=='0' && s[1]=='7'){s[0] = '1'; s[1] = '9';}
    if (s[0]=='0' && s[1]=='8'){s[0] = '2'; s[1] = '0';}
    if (s[0]=='0' && s[1]=='9'){s[0] = '2'; s[1] = '1';}
    if (s[0]=='1' && s[1]=='0'){s[0] = '2'; s[1] = '2';}
    if (s[0]=='1' && s[1]=='1'){s[0] = '2'; s[1] = '3';}
  }
  
  cout << s;
}